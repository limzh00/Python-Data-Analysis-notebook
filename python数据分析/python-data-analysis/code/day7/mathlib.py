import math

def add(x, y):
    return x + y

def sub(x, y):
    return x - y

def mul(x, y):
    return x*y

def div(x, y):
    return x / y

def rem(x, y):
    return x % y

def sqrt(x):
    return math.sqrt(x)

def power(x, y):
    return math.pow(x, y)

def sin(x):
    return math.sin(x)

def cos(x):
    return math.cos(x)
